def print_summary(variant_name, results, baseline_anomalous=None):
    anom = [r for r in results if r["ground_truth"] == "anomalous"]
    norm = [r for r in results if r["ground_truth"] == "normal"]

    tp = sum(1 for r in anom if r["vlm_is_anomalous"] is True)
    fn = sum(1 for r in anom if r["vlm_is_anomalous"] is False)
    tn = sum(1 for r in norm if r["vlm_is_anomalous"] is False)
    fp = sum(1 for r in norm if r["vlm_is_anomalous"] is True)

    sens = tp / len(anom) if anom else 0
    spec = tn / len(norm) if norm else float("nan")

    avg_anom_score = (
        sum(r["vlm_anomaly_score"] for r in anom if r["vlm_anomaly_score"] is not None)
        / max(1, sum(1 for r in anom if r["vlm_anomaly_score"] is not None))
    )
    avg_norm_score = (
        sum(r["vlm_anomaly_score"] for r in norm if r["vlm_anomaly_score"] is not None)
        / max(1, sum(1 for r in norm if r["vlm_anomaly_score"] is not None))
    ) if norm else float("nan")

    print(f"\n{'='*60}")
    print(f"  {variant_name}")
    print(f"{'='*60}")
    print(f"  Sensitivity (anomalous detected):  {tp}/{len(anom)}  ({sens:.0%})")
    if norm:
        print(f"  Specificity (normal correct):      {tn}/{len(norm)}  ({spec:.0%})")
        print(f"  False positives:                   {fp}")
    print(f"  False negatives:                   {fn}")
    print(f"  Avg VLM score (anomalous):         {avg_anom_score:.1f}/10")
    if norm:
        print(f"  Avg VLM score (normal):            {avg_norm_score:.1f}/10")

    if baseline_anomalous is not None:
        print(f"\n  --- Flips from baseline ---")
        baseline_map = {(r["video"], r["frame_idx"]): r for r in baseline_anomalous}
        for r in anom:
            key = (r["video"], r["frame_idx"])
            if key in baseline_map:
                bl = baseline_map[key]
                bl_det = bl.get("vlm_is_anomalous", False)
                new_det = r["vlm_is_anomalous"]
                if bl_det != new_det:
                    direction = "MISSED->DETECTED" if new_det else "DETECTED->MISSED"
                    print(f"    {direction}: {r.get('source_folder')} "
                          f"(baseline_score={bl.get('vlm_anomaly_score')}, "
                          f"new_score={r['vlm_anomaly_score']})")


def main():
    print("=== VLM Experiment V2: MAE Score + Heatmap Variants ===\n")

    if not os.environ.get("OPENAI_API_KEY"):
        print("ERROR: Set OPENAI_API_KEY environment variable first.")
        sys.exit(1)

    baseline = load_baseline()
    baseline_anom = [r for r in baseline if r["ground_truth"] == "anomalous"]

    anomalous = get_anomalous_frames()
    normal = get_normal_frames()
    all_frames = anomalous + normal

    print(f"Frames: {len(anomalous)} anomalous, {len(normal)} normal\n")

    # --- Variant A: frame + MAE score ---
    print("--- Variant A: Frame + MAE anomaly score ---")
    results_a = run_variant_a(all_frames)

    # --- Variant B: frame + heatmap (anomalous only) ---
    print("\n--- Variant B: Frame + heatmap overlay ---")
    results_b = run_variant_b(all_frames)

    # --- Variant C: frame + MAE score + heatmap (anomalous only) ---
    print("\n--- Variant C: Frame + MAE score + heatmap ---")
    results_c = run_variant_c(all_frames)

    # --- Summary ---
    print("\n\n" + "#" * 60)
    print("  COMPARISON SUMMARY")
    print("#" * 60)

    print_summary("BASELINE (frame only)", baseline, baseline_anom)
    print_summary("VARIANT A (frame + MAE score)", results_a, baseline_anom)
    print_summary("VARIANT B (frame + heatmap)", results_b, baseline_anom)
    print_summary("VARIANT C (frame + MAE score + heatmap)", results_c, baseline_anom)

    all_results = {
        "baseline_summary": {
            "tp": sum(1 for r in baseline_anom if r.get("vlm_is_anomalous") is True),
            "total_anomalous": len(baseline_anom),
        },
        "variant_a": results_a,
        "variant_b": results_b,
        "variant_c": results_c,
    }

    with open(OUTPUT_PATH, "w") as f:
        json.dump(all_results, f, indent=2)

    print(f"\n\nAll results saved to: {OUTPUT_PATH}")


if __name__ == "__main__":
    main()
