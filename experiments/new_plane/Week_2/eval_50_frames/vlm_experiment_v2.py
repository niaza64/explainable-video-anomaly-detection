def encode_image_b64(path):
    with open(path, "rb") as f:
        return base64.b64encode(f.read()).decode("utf-8")


def make_image_content(path):
    ext = Path(path).suffix.lower()
    media_type = "image/png" if ext == ".png" else "image/jpeg"
    b64 = encode_image_b64(path)
    return {
        "type": "image_url",
        "image_url": {"url": f"data:{media_type};base64,{b64}"},
    }


def query_vlm(prompt_text, image_paths):
    from openai import OpenAI
    client = OpenAI()

    content = [{"type": "text", "text": prompt_text}]
    for p in image_paths:
        content.append(make_image_content(p))

    try:
        response = client.chat.completions.create(
            model="gpt-4o",
            messages=[{"role": "user", "content": content}],
            max_tokens=300,
            response_format={"type": "json_object"},
        )
        return json.loads(response.choices[0].message.content)
    except Exception as e:
        print(f"  ERROR: {e}")
        return {"description": f"ERROR: {e}", "is_anomalous": None, "anomaly_explanation": None, "anomaly_score": None}


def load_baseline():
    with open(EVAL_DIR / "vlm_experiment_results.json") as f:
        return json.load(f)


def get_anomalous_frames():
    """Get the same 20 anomalous frames used in baseline."""
    with open(EVAL_DIR / "manifest.json") as f:
        manifest = json.load(f)
    sorted_by_score = sorted(manifest, key=lambda x: x["anomaly_score"], reverse=True)[:20]
    frames = []
    for entry in sorted_by_score:
        folder = EVAL_DIR / entry["folder"]
        original = folder / "original.png"
        overlay = folder / "overlay.png"
        heatmap = folder / "heatmap.png"
        if original.exists():
            frames.append({
                "video": entry["video"],
                "scene": entry["scene"],
                "frame_idx": entry["frame_idx"],
                "mae_score": entry["anomaly_score"],
                "ground_truth": "anomalous",
                "source_folder": entry["folder"],
                "original_path": str(original),
                "overlay_path": str(overlay) if overlay.exists() else None,
                "heatmap_path": str(heatmap) if heatmap.exists() else None,
            })
    return frames


def get_normal_frames():
    """Get the same 20 normal frames used in baseline."""
    baseline = load_baseline()
    frames = []
    for entry in baseline:
        if entry["ground_truth"] == "normal" and entry.get("image_path"):
            frames.append({
                "video": entry["video"],
                "scene": entry["scene"],
                "frame_idx": entry["frame_idx"],
                "mae_score": 0.15,
                "ground_truth": "normal",
                "source_folder": None,
                "original_path": entry["image_path"],
                "overlay_path": None,
                "heatmap_path": None,
            })
    return frames


def run_variant_a(frames):
    """Variant A: frame + MAE score in prompt."""
    results = []
    for i, f in enumerate(frames):
        tag = f"[{f['ground_truth'].upper():>9s}]"
        src = f.get("source_folder") or f"{f['video']}/frame{f['frame_idx']}"
        print(f"  A ({i+1}/{len(frames)}) {tag} {src} mae={f['mae_score']:.3f} ...", end=" ", flush=True)

        if f["ground_truth"] == "anomalous":
            prompt = PROMPT_A.format(mae_score=f["mae_score"])
        else:
            prompt = PROMPT_A_NORMAL.format(mae_score=f["mae_score"])

        vlm = query_vlm(prompt, [f["original_path"]])
        print(f"score={vlm.get('anomaly_score')}  anom={vlm.get('is_anomalous')}")

        results.append({**f, "variant": "A_mae_score",
                        "vlm_description": vlm.get("description"),
                        "vlm_is_anomalous": vlm.get("is_anomalous"),
                        "vlm_anomaly_explanation": vlm.get("anomaly_explanation"),
                        "vlm_anomaly_score": vlm.get("anomaly_score")})
        time.sleep(0.3)
    return results


def run_variant_b(frames):
    """Variant B: frame + heatmap overlay (anomalous only)."""
    results = []
