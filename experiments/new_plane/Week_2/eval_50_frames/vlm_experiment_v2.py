"""
VLM Experiment V2: Test whether MAE score and/or heatmap improves detection.

Runs three variants on the same 20 anomalous frames + 20 normal frames:
  A) Frame + MAE anomaly score in prompt
  B) Frame + heatmap overlay image
  C) Frame + MAE score + heatmap

Compares against baseline results from vlm_experiment_results.json.

Usage:
    export OPENAI_API_KEY="sk-..."
    python vlm_experiment_v2.py
"""

